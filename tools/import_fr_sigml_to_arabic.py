# Directory containing the files to rename
directory = r'C:\Users\LENOVO\Desktop\sigml'
 

# Get the list of files in the directory
files = os.listdir(directory)

# Iterate over each file in the directory
for filename in files:
    # Get the base name without the extension
    base_name = os.path.splitext(filename)[0]
    # Check if the base name is in the dictionary
    if base_name in words_translations:
        # Get the translation
        translation = words_translations[base_name]
        # Form the new filename
        new_filename = f"{translation}.sigml"
        # Get the full paths
        old_file = os.path.join(directory, filename)
        new_file = os.path.join(directory, new_filename)
        # Rename the file
        os.rename(old_file, new_file)
        print(f"Renamed '{filename}' to '{new_filename}'")
    else:
        print(f"No translation found for '{filename}'")



